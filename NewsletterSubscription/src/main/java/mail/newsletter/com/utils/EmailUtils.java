    public static void sendMailBulk(List<String> emails, String from, String body, String subject) throws AddressException {

        try {
            InternetAddress fromAddress = new InternetAddress(from);
            InternetAddress[] bulkAddresses = new InternetAddress[emails.size()];

            int i = 0;
            for (String mail: emails) {
                bulkAddresses[i] = new InternetAddress( mail);
                i++;
            }

            System.setProperty("file.encoding","UTF-8");
            System.setProperty("client.encoding","UTF-8");

            MailMessage mailMessage =  new MailMessage();
            mailMessage.setFrom(fromAddress);
            mailMessage.setBulkAddresses(bulkAddresses);
            mailMessage.setSubject(subject);
            mailMessage.setBody(body);
            mailMessage.setHTMLFormat(true);

            MailServiceUtil.sendEmail(mailMessage);
            System.out.println("Send successfully");
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }
